class Solution:
    def twoSum(self, nums: List[int], target: int) -> List[int]:
        # loop through each num in list, for each num, loop through again from next num
        # add and see if they equal target, if not move on, if so return the index.
        for i in range(len(nums)):
            for j in range(i + 1, len(nums)):
                if nums[i] + nums[j] == target:
                    return [i, j]

        return []
        # time: O(n^2)
        # space: O(1)
